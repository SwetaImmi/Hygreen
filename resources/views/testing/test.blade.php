    function search() {
      $('.loader-image').show();
      if ($('.search-expand > input').val() != '') {
        $('#formsearch').submit();
        $.ajax({
          url: "https://hygreen.qa/products/search",
          type: 'post',
          data: {
            val: $('.search-expand > input').val()
          },
          success: function(data) {
            $('body').html(data);
            $.noConflict();
          }
        })
      }
    }

    function searchByAutocomplete() {
      $('.loader-image').show();
      if ($('.search-expand  input').val() != '') {
        data = $('.search-expand  input').val();
        $.ajax({
          url: "https://hygreen.qa/products/search-detail",
          type: 'post',
          data: {
            val: data
          },
          success: function(data) {
            // window.location.href="https://hygreen.qa/products/details/?name="+data
            window.location.href = "https://hygreen.qa/products/item/" + data
          }
        })
      }
    }

